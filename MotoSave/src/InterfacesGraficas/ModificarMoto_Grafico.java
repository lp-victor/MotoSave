    private void B_Modificar_ModificarMotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_B_Modificar_ModificarMotoActionPerformed
        String sucursal = (String) CB_sucursal_ModificarMoto.getSelectedItem();
        String matricula = "";
        String color = (String) CB_color_ModificarMoto.getSelectedItem();
        int cilindrada = 0;
        int precio = 0;

        // Control matricula
        if (!INTERFACES.Entradable.validarMatricula(TF_matricula_ModificarMoto.getText())) {
            JOptionPane.showMessageDialog(this, "La matricula no cumple con el formato requerido. Ej: 1234ABC", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            matricula = TF_matricula_ModificarMoto.getText();
        }

        // Control CC y Precio
        if (INTERFACES.Entradable.esInt(TF_CC_ModificarMoto.getText()) && INTERFACES.Entradable.esInt(TF_precio_ModificarMoto.getText())) {
            cilindrada = Integer.parseInt(TF_CC_ModificarMoto.getText());
            precio = Integer.parseInt(TF_precio_ModificarMoto.getText());
        } else {
            JOptionPane.showMessageDialog(this, "Solo puede introducir numeros en los campos 'CC' y 'Precio'.", "Error", JOptionPane.ERROR_MESSAGE);
        }

        Motocicleta motoModificada = new Motocicleta(sucursal2Id(sucursal), matricula, moto.getMarca(), moto.getModelo(), color, cilindrada, precio);

        // Guardamos la moto dependiendo del tipo de persistencia que se haya seleccionado en el login.
        if (tipoPers.equals(Enumerados.tipoDAO.JDBC_MOTOCICLETA.toString())) {
            try {
                motoDAO.modificarMoto(motoModificada);
            } catch (MotocicletaExcepcion e) {
                JOptionPane.showMessageDialog(this, "No se ha podido moficar la motocicleta.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            try {
                serialMoto.modificarMoto(motoModificada);
            } catch (MotocicletaExcepcion e) {
                JOptionPane.showMessageDialog(this, "No se ha podido moficar la motocicleta.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }

        this.dispose();
    }//GEN-LAST:event_B_Modificar_ModificarMotoActionPerformed

